// Get the users 'profile' information
exports.getUserProfile = async (req, res) => {
    if (!req.params.username) {
        res.sendStatus(400);
        return;
    }
    const username = req.params.username;
    try {
        const user = await this.readOneMethod({'username': username});
        res.status(200).json({
            _id: user._id,
            username: user.username,
            firstName: user.firstName,
            lastName: user.lastName,
            email: user.email,
            phone: user.phone,
            role: user.role,
        });
    } catch (err) {
        console.log(err);
        res.sendStatus(500);
    }
};
